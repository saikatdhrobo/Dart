// Conditional Statement if, if else, if else if

void main(){
    var marks = 70;
    if(marks>=80){
      print("Result is A+");
    }
    else if(marks<80 && marks>=70){
      print("Result is A");
    }
    else if(marks<70 && marks>=60){
      print("result is A-");
    
    }
    else{
      print("Result is not A+ or A or A-");
    }
  print("Thank you");
}